    public Runnable newAtualizaEstadosAction() {
        return new Runnable() {
            public void run() {
                try {
                    IEstado iEstado = new EstadoDAO();
                    List<Object[]> listEstados = iEstado.getAll();
                    String strEstado = "";
                    
                    List<Estado> result = new ArrayList<>(listEstados.size());
                    
                    for (Object[] row : listEstados) {
                        result.add(new Estado(null, (String) row[0], (String) row[1]));
                    }
                    
                    for (Estado estado : result) {
                        strEstado += estado.getSigla() + " - " + estado.getNome() + "\n";
                    }
                                        
                    painelEstados.setText(strEstado);
                } catch (PersistenceException ex) {
                    JOptionPane.showMessageDialog(CadastroEstado.this,
                        ex.getMessage(), "Erro ao consultar Estado(s)", JOptionPane.ERROR_MESSAGE);
                }
            }
        };
    }
    
    enum TipoBotao {
        incluir,
        editar,
        excluir
    }
    
    private void habilitarDesabilitarCampos(TipoBotao tipo) {
        limpaCampos();
        
        if (null == tipo) {
            campoId.setEnabled(false);
            campoSigla.setEnabled(false);
            campoNome.setEnabled(false);
            botaoBuscar.setEnabled(false);
            botaoSalvar.setEnabled(false);
            botaoDeletar.setEnabled(false);
        } else switch (tipo) {
            case incluir:
                campoId.setEnabled(false);
                campoSigla.setEnabled(true);
                campoNome.setEnabled(true);
                botaoBuscar.setEnabled(false);
                botaoSalvar.setEnabled(true);
                botaoDeletar.setEnabled(false);
                break;
            case editar:
                campoId.setEnabled(true);
                campoSigla.setEnabled(true);
                campoNome.setEnabled(true);
                botaoBuscar.setEnabled(true);
                botaoSalvar.setEnabled(true);
                botaoDeletar.setEnabled(false);
                break;
            case excluir:
                campoId.setEnabled(true);
                campoSigla.setEnabled(false);
                campoNome.setEnabled(false);
                botaoBuscar.setEnabled(true);
                botaoSalvar.setEnabled(false);
                botaoDeletar.setEnabled(true);
                break;
            default:
                campoId.setEnabled(false);
                campoSigla.setEnabled(false);
                campoNome.setEnabled(false);
                botaoBuscar.setEnabled(false);
                botaoSalvar.setEnabled(false);
                botaoDeletar.setEnabled(false);
                break;
        }
    }
    
    private void limpaCampos() {
        campoId.setText("");
        campoSigla.setText("");
        campoNome.setText("");
    }
    
