    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        List<String> listaObrigatorio = Validacao.ValidaCamposEstado(campoNome.getText(), campoSigla.getText());
                
        if (listaObrigatorio.size() > 0) {
            String msg = "";
            msg = listaObrigatorio.stream().map((str) -> str + "\n").reduce(msg, String::concat);
            JOptionPane.showMessageDialog(this, msg, "Validação", JOptionPane.WARNING_MESSAGE);
        } else {
            if (campoId.isEnabled()) {
                if(!Utils.isNumber(campoId.getText())) {
                    JOptionPane.showMessageDialog(this, "Preencher o campo Id válido.", 
                            "Validação", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            
            Estado e = new Estado(campoId.getText().isBlank() ? null : Integer.parseInt(campoId.getText()) , campoSigla.getText().trim(), campoNome.getText().trim());   
            IEstado iEstado = new EstadoDAO();
            iEstado.save(e);
//            EstadoDAO estadoDAO = new EstadoDAO();
//            estadoDAO.save(e);
            
            SwingUtilities.invokeLater(newAtualizaEstadosAction());
            
            habilitarDesabilitarCampos(null);
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void botaoDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoDeletarActionPerformed
        if(Utils.isNumber(campoId.getText())) {
            try {
                IEstado iEstado = new EstadoDAO();
                Estado e = iEstado.findById(Integer.parseInt(campoId.getText()));
                if (e != null) {
                    iEstado.remove(e);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, 
                        ex.getMessage(), "Erro ao excluir Estado.", JOptionPane.ERROR_MESSAGE);
            }
            
            SwingUtilities.invokeLater(newAtualizaEstadosAction());
            habilitarDesabilitarCampos(null);
        } else {
            JOptionPane.showMessageDialog(this, "Preencher o campo Id válido.", 
                    "Validação", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botaoDeletarActionPerformed

