    private void botaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoBuscarActionPerformed
        if (Utils.isNumber(campoId.getText())) {
           try {
                IEstado iEstado = new EstadoDAO();
                Estado e = iEstado.findById(Integer.parseInt(campoId.getText()));
                if (e != null) {
                    campoSigla.setText(e.getSigla());
                    campoNome.setText(e.getNome());
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, 
                        ex.getMessage(), "Erro ao pesquisar Estado.", JOptionPane.ERROR_MESSAGE);
            } 
        } else {
            JOptionPane.showMessageDialog(this, "Digite nÃºmero do Id vÃ¡lido.", 
                    "Campo Id", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botaoBuscarActionPerformed

